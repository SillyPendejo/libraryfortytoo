#include <string.h>

void	ft_memswap(void *s1, void *s2, size_t n)
{
	char	tmp;
	char	*c1;
	char	*c2;

	if (!s1 || !s2 || n <= 0)
		return ;
	c1 = (char *)s1;
	c2 = (char *)s2;
	while (n--)
	{
		tmp = *c1;
		*c1 = *c2;
		*c2 = tmp;
		c1++;
		c2++;
	}
}
