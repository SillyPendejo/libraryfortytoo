int		ft_atoi(const char *str)
{
	int i;
	int neg;
	int nb;

	nb = 0;
	neg = 1;
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
			|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
	{
		neg = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		if (neg == -1 && nb == 214748364 && str[i] == '8')
			return (-2147483648);
		nb = str[i] - '0' + nb * 10;
		i++;
	}
	return (nb * neg);
}
