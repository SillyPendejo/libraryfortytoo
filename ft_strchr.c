#include <string.h>

char	*ft_strchr(const char *s, int c)
{
	int i;

	i = -1;
	while (s[++i])
	{
		if (s[i] == (char)c)
			return ((char *)s + i);
	}
	if ((char)c == '\0')
	{
		if (((char *)s + i + 1)[0] == '\0')
			return ((char *)s + i + 1);
	}
	return (NULL);
}
